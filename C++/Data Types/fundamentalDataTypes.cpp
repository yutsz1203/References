int main(){
    // int
    int num = 42;
    short int shotnum = 1; // smaller range than int
    long int longnum = 333; // larger range than int
    long long int longlongnum = 2343489; // even larger range than long int

    // float, double
    float pi = 3.14f; // single precision floating-point numbers, 4 bytes of memory
    double pi_high_precision = 3.1415926535; // double precision floating-point numbers, 8 bytes of memory

    // char
    char letter = 'A'; // single quote

    // bool
    bool boolean = true;

    return 0;
}